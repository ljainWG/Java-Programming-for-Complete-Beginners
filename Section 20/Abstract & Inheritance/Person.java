
public class Person {
	String name, fName, mName, address, phoneNo, eMail;

	public Person(String name, String fName, String mName, String address, String phoneNo, String eMail) {
		super();
		this.name = name;
		this.fName = fName;
		this.mName = mName;
		this.address = address;
		this.phoneNo = phoneNo;
		this.eMail = eMail;
	}

}
